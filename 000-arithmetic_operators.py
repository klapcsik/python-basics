"""
Present arithmetic operators
"""

x = 7
y = 3

# Addition: +
# x + y
print('Addition: {} + {} = {}'.format(x, y, (x + y)))

# Subtraction: -
# x - y
print('Subtraction: {} - {} = {}'.format(x, y, (x - y)))

# Multiplication: *
# x * y
print('Multiplication: {} * {} = {}'.format(x, y, (x * y)))

# Division: /
# x / y
print('Division: {} / {} = {}'.format(x, y, (x / y)))

# Exponentiation: **
# x ** y
print('Exponentiation: {} ** {} = {}'.format(x, y, (x ** y)))

# Floor division: //
# x // y
print('Floor division: {} // {} = {}'.format(x, y, (x // y)))

# Modulus: %
# x % y
print('Modulus: {} % {} = {}'.format(x, y, (x % y)))