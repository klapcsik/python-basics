"""
Return the binary version of any integer
----------------------------------------

Input:      (int)  any integer

Return:     binary version of integer
"""

int_1 = 1
print('Integer: {}\nBinary version: {}'.format(int_1, bin(int_1)))

int_2 = 10
print('\nInteger: {}\nBinary version: {}'.format(int_2, bin(int_2)))

int_3 = -10
print('\nInteger: {}\nBinary version: {}'.format(int_3, bin(int_3)))

int_4 = 0
print('\nInteger: {}\nBinary version: {}'.format(int_4, bin(int_4)))